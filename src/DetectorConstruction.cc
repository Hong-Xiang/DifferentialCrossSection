G4VPhysicalVolume* DetectorConstruction::Construct()
{
    G4NistManager* nistManager = G4NistManager::Instance();

    G4Material* water = nistManager->FindOrBuildMaterial("G4_WATER");
    G4Material* vacuum = nistManager->FindOrBuildMaterial("G4_Galactic");
    G4Box* worldBox = new G4Box("WorldBox",10*m,10*m,10*m);
    G4LogicalVolume* worldLog = new G4LogicalVolume(worldBox,vacuum,"WorldLog");
    G4PVPlacement* world = new G4PVPlacement(0,G4ThreeVector(),worldLog,"World",0,false,0);
    G4Box* phantomBox = new G4Box("PhantomBox",10*m,10*m,10*m);
    G4LogicalVolume* phantomLog = new G4LogicalVolume(phantomBox,water,"WorldLog");
    G4PVPlacement* phantom = new G4PVPlacement(0,G4ThreeVector(),phantomLog,"Phantom",worldLog,false,0);
    return world;
}
